class Clienti:
    def __init__(self, cf, nome, cognome, citta): 
        self.cf = cf
        self.nome = nome
        self.cognome = cognome
        self.citta = citta


def get_cd(self):
        return self.__cf
def get_nome(self):
        return self.__nome
def get_cognome(self):
        return self.__cognome
def get_citta(self):
        return self.__citta
